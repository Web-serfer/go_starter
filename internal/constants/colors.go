package constants

// Цвета и эффекты для текста в терминале
const (
	// Сброс всех настроек
	Reset = "\033[0m"

	// Стили текста
	Bold      = "\033[1m" // Жирный
	Underline = "\033[4m" // Подчеркнутый

	// Основные цвета
	Red     = "\033[31m" // Ошибки, жизни
	Green   = "\033[32m" // Правильные ответы
	Yellow  = "\033[33m" // Подсказки, важные факты
	Blue    = "\033[34m" // Вопросы
	Magenta = "\033[35m" // Фиолетовый (для разнообразия)
	Cyan    = "\033[36m" // Заголовки, интересные факты
	White   = "\033[37m" // Обычный белый

	// Дополнительные цвета
	Gray = "\033[90m" // Серый (для статус-бара, второстепенного текста)
)

// Цвета фона (если захочешь сделать кнопки или плашки)
const (
	BgRed    = "\033[41m"
	BgGreen  = "\033[42m"
	BgYellow = "\033[43m"
	BgBlue   = "\033[44m"
)
